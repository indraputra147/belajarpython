x = int(input("Input a number of sides (> 3):"))

if x == 3:
    print("It's Triangle!")
elif x == 4:
    print("It's Rectangle!")
elif x == 5:
    print("It's Pentagon!")
elif x == 6:
    print("It's Hexagon!")
elif x == 7:
    print("It's Heptagon!")
elif x == 8:
    print("It's Octagon!")
elif x == 9:
    print("It's Nonagon!")
elif x == 10:
    print("It's Decagon!")
else:
    print("The number of side is out of range!")
